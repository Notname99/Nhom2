<p>Kể từ đó, <a href="../../index.php?quanly=Nike&id=1&per_page=12&page=1" target="_blank"><b>giày Nike Air Max</b></a> thế hệ tiếp theo như Air Max 90, Air Max 95 do Tinker Hatfield thiết kế đã trở thành điểm nhấn với các vận động viên và các Sneakerhead trên toàn thế giới.</p>
